    public String LoginDoctor(int DNI) {
        System.out.println("Mostrando Datos");
        Conexion objConexion = new Conexion();
        String strSentenciaInsert = String.format("SELECT NOMBRE FROM DOCTOR WHERE DNI = %d", DNI);
        try {
            ResultSet resultado = objConexion.consultarRegistros(strSentenciaInsert);
            if (resultado.getString("NOMBRE") != null) {
                nombre = resultado.getString("NOMBRE");
            } else {
                nombre = null;
            }
        } catch (Exception e) {
        }

        return nombre;
    }

