    public MenuAdmin(int privilegio, int DNI) {
        initComponents();
        //  this.setExtendedState(MAXIMIZED_BOTH); --maximiza la ventaza

        this.privilegio = privilegio;
        this.DNI = DNI;
        if (LoginDoctor(this.DNI) != null) {
            nombre = LoginDoctor(this.DNI);
        } else if (LoginPaciente(this.DNI) != null) {
            nombre = LoginPaciente(this.DNI);
        } else {
            nombre = LoginSecretaria(this.DNI);
        }
        switch (this.privilegio) {
            case 1: {
                btnCita.setVisible(true);
                btnConsultorio.setVisible(false);
                btnDoctor.setVisible(false);
                btnHistorial.setVisible(true);
                btnHorario.setVisible(false);
                btnPaciente.setVisible(false);
                btnSecretaria.setVisible(false);
                txtUsuario.setText(nombre);
                break;
            }
            case 2: {
                btnCita.setVisible(true);
                btnConsultorio.setVisible(false);
                btnDoctor.setVisible(true);
                btnHistorial.setVisible(true);
                btnHorario.setVisible(true);
                btnPaciente.setVisible(true);
                btnSecretaria.setVisible(false);
                txtUsuario.setText(nombre);
                break;
            }
            case 3: {
                txtUsuario.setText(nombre);
                break;
            }
        }
    }

